"""
rti_lib/devices/u2/stream_profile.py

U2 base device-stream builder.  Generates the opening TLV records for an
RTI U2 display-based handheld remote (64x128 B&W LCD, 2x4 icon grid).

Structure (351 TLV records):
  Device config (44)   NTP / location   Identity
  9 input-slot indices   4 page-group IDs
  16 group slots   8 state blobs   256 variable slots
  Home page container (TAG=01, with fixed title "Home Page")
"""

